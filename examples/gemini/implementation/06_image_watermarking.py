    print("   5. Tampering detection via similarity scoring")

    print("\n📁 Directory Structure:")
    print(f"   {output_dir}/")
    print("   ├── 01_original.png      (AI-generated image)")
    print("   ├── 02_watermarked.png   (With CIAF watermark + QR)")
    print("   ├── 03_evidence.json     (Forensic evidence)")
    print("   └── 04_modified.png      (Tampered version)")

    print("\n🎯 Best Practices:")
    print("   - Watermark immediately after generation")
    print("   - Store original + watermarked + evidence")
    print("   - Use QR codes for public verification")
    print("   - Monitor confidence scores for tampering")
    print("   - Archive evidence for forensic analysis")

    print("\n📚 Related Examples:")
    print("   - 01_basic_text_watermarking.py - Text watermarking")
