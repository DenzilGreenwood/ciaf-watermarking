    print(f"   - Watermarked size: {len(watermarked_image_bytes):,} bytes")
    print(f"   - Modified size: {len(modified_image_bytes):,} bytes")
    print(f"   - Artifacts saved: {len(files_saved)} files")
    print(f"   - Output directory: {output_dir}/")

    print("\n🔍 Verification Results:")
    print(f"   - Original: {result_original_confidence:.2%} confidence")
    print(f"   - Modified: {result_modified_confidence:.2%} confidence")

